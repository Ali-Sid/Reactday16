// 3. What is the difference between higher order function and higher order component?
/**  The main difference is in their application and purpose:

Higher Order Function: It is a function that takes one or more functions as arguments or returns a function as its result.
Higher Order Component: It is a function that takes a component as an argument and returns a new enhanced component with additional props or behavior.

In summary, a higher order function operates on regular functions, whereas a higher order component operates on React components. */

